void PrintASCII(char string[])
{
	for(int i=0; i<=string[i];i++)
	{
		printf("%c ",string[i]);
	}
	printf("\n");

	for(int i=0; i<=string[i];i++)
	{
		printf("%x ",string[i]);
	}
	printf("\n");

	for(int i=0; i<=string[i]; i++)
	{
		printf("%d ",string[i]);
	}
	printf("\n");
}
